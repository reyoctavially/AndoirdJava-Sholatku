package org.d3ifcool.sholatku.data;

public class Doa {
    String judul, subjudul;

    public Doa(String judul, String subjudul) {
        this.judul = judul;
        this.subjudul = subjudul;
    }

    public String getJudul() {
        return judul;
    }

    public String getSubjudul() {
        return subjudul;
    }
}
